public class OperatorAritmatika {
    public static void main(String args []) {
        System.out.println("1 + 2 x 3 = "+(1+2*3));
        System.out.println("13 % 5    = "+(13%5));
        System.out.println("13 % 5.2  = "+(13%5.2));
        System.out.println("13 / 5    = "+(13/5));
        System.out.println("13 / 5.2  = "+(13/5.2));
    }
}