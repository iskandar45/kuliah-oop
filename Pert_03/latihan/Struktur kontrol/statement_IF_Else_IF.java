public class statement_IF_Else_IF {
    public static void main(String [] args) {
        int nilai = 95;
        if (nilai > 90) {
            System.out.println("Sangat Bagus !");
        } else if (nilai > 60) {
            System.out.println("Bagus !");
        } else {
            System.out.println("Maaf Anda Gagal !");
        }
    }
}